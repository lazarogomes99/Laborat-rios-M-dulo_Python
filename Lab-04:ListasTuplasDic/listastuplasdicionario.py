listaDeFrutas = ["Maça", "Banana", "Morango"]
print(listaDeFrutas)
print(type(listaDeFrutas))
print("==============================================")


# Acessando uma lista pela posição

print(listaDeFrutas[0])
print(listaDeFrutas[1])
print(listaDeFrutas[2])
print("==============================================")

# Alteração de valores da lista

listaDeFrutas[2] = "orange"
print(listaDeFrutas)
print("==============================================")

